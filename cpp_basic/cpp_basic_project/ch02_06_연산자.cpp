# include <iostream>
using namespace std;

int main()
{
	int num1 = 10, num2 = 4;
	int result;

	result = num1 / num2;

	cout << "10 / 4 = 2 ";
	cout << endl;
	cout << num1 << " / " << num2 << " = " << result << endl;

	return 0;

}

